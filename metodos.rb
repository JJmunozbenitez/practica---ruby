# Un metodo o tambien llamado Funcion es un bloque de codigo que contiene una serie de instruccciones.
# para definir un metodo hay que usar la palabra reservada def:

def nombre_del_metodo
  puts "Hola, yo soy el metodo"
end # finalizacion del bloque de codigo

# para poder ejecutar la informacion que tengo en mi Funcion, Tenemos que invocarlo:

nombre_del_metodo() # acá lo invocamos, los paréntesis son opcionales

